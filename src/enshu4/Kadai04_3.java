    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try{
            String driveUrl = "jdbc:derby://localhost:1527/meibo";
            String sql = "select * from t_student where gakubu_id=?";
            Connection con = DriverManager.getConnection(driveUrl,"db","db");
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.setString(1, gakubu_input.getText());
            ResultSet rs = pstmt.executeQuery();
            
            resultField.setText("");
            while (rs.next()) {
                resultField.append(Integer.toString(rs.getInt("student_id")) + ",");
                resultField.append(rs.getString("fullname") + ",");
                resultField.append(Integer.toString(rs.getInt("gakubu_id")) + ",");
                resultField.append(Integer.toString(rs.getInt("grade"))+ ",");
                resultField.append(Integer.toString(rs.getInt("score")) + "\n");
            }
            con.close();
            pstmt.close();
            rs.close();
        } catch (SQLException e) {
            resultField.setText(e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void calcBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcBtnActionPerformed
        // TODO add your handling code here:
        try{
            String driveUrl = "jdbc:derby://localhost:1527/meibo";
            String sql = "select gakubu_id,max(score) as \"max\" from t_student group by gakubu_id";
            Connection con = DriverManager.getConnection(driveUrl,"db","db");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            resultField.setText("学部ID:最高点\n");
            while (rs.next()) {
                resultField.append(rs.getInt("gakubu_id") + ":" + rs.getInt("max") + "\n");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            resultField.setText(e.getMessage());
        }
    }//GEN-LAST:event_calcBtnActionPerformed

